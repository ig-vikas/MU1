function readStoredLang() {
  try {
    const stored = globalThis.localStorage?.getItem(LANG_KEY);
    return SUPPORTED_LANGS.has(stored) ? stored : 'en';
  } catch (error) {
    return 'en';
  }
}

let currentLang = readStoredLang();

/**
 * Sets the active JanVaani language.
 * @param {'en' | 'hi' | 'te' | string} code - Language code.
 * @returns {string} Active language code.
 */
export function setLang(code) {
  try {
    currentLang = SUPPORTED_LANGS.has(code) ? code : 'en';
    globalThis.localStorage?.setItem(LANG_KEY, currentLang);
    return currentLang;
  } catch (error) {
    currentLang = 'en';
    return currentLang;
  }
}

/**
 * Gets the active JanVaani language.
 * @returns {string} Active language code.
 */
export function getLang() {
  try {
    return currentLang;
  } catch (error) {
    return 'en';
  }
}

/**
 * Translates a key using the active language.
 * @param {string} key - Translation key.
 * @param {Record<string, string | number>} params - Template parameters.
 * @returns {string} Localized text.
 */
export function t(key, params = {}) {
  try {
    const table = translations[currentLang] ?? translations.en;
    const fallback = translations.en[key] ?? key;
    const template = table[key] ?? fallback;

    return String(template).replace(/\{(\w+)\}/g, (_, name) => String(params[name] ?? `{${name}}`));
  } catch (error) {
    return key;
  }
}
